a = 12
b = 8
c = 2.5

# zbroj
print("a + b =", a + b)

# razlika
print("a - b =", a - b)

# umnožak
print("a * b =", a * b)

# količnik
print("a / b =", a / b)

# cijelobrojno dijeljenje
print("a // b =", a // b)

# ostatak pri dijeljenju
print("a % b =", a % b)

# potenciranje
print("a ** c =", round(a ** c, 2))
